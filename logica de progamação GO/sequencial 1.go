package main

import (
	"fmt"
)

func main() {
	var num1, num2, num3, soma int

	// Lendo os três números
	fmt.Print("Digite o primeiro número: ")
	fmt.Scan(&num1)

	fmt.Print("Digite o segundo número: ")
	fmt.Scan(&num2)

	fmt.Print("Digite o terceiro número: ")
	fmt.Scan(&num3)

	// Calculando a soma
	soma = num1 + num2 + num3

	// Mostrando o resultado
	fmt.Printf("A soma de %d, %d e %d é: %d\n", num1, num2, num3, soma)
}
